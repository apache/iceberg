package org.apache.iceberg.rest.auth.oauth2.config;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * An annotation that can be used to mark a method as a configuration option.
 *
 * <p>This annotation can be leveraged by documentation generators to extract the configuration
 * options and their descriptions.
 */
@Retention(RetentionPolicy.CLASS)
@Target(ElementType.METHOD)
public @interface ConfigOption {

  /** The fully-qualified name of the configuration option. */
  String value();

  /** Whether this option is a prefix map option (accepting multiple values). */
  boolean prefixMap() default false;
}
